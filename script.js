function applyLanguage(lang) {
  localStorage.setItem("lang", lang);
  document.documentElement.lang = lang === "jp" ? "ja" : "en";

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (translations[lang] && translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });

  const jpBtn = document.getElementById("lang-jp");
  const enBtn = document.getElementById("lang-en");

  if (jpBtn && enBtn) {
    jpBtn.classList.toggle("active", lang === "jp");
    enBtn.classList.toggle("active", lang === "en");
  }

  const amountInput = document.getElementById("amountInput");
  if (amountInput) {
    amountInput.placeholder = lang === "jp" ? "10000" : "10000";
  }
}

async function convertCurrency() {
  const currencySelect = document.getElementById("currencySelect");
  const amountInput = document.getElementById("amountInput");
  const status = document.getElementById("exchangeStatus");
  const resultBox = document.getElementById("exchangeResult");
  const inputSummary = document.getElementById("inputSummary");
  const rateSummary = document.getElementById("rateSummary");
  const convertedSummary = document.getElementById("convertedSummary");

  const lang = localStorage.getItem("lang") || "jp";

  if (!currencySelect || !amountInput || !status) return;

  const currency = currencySelect.value;
  const amount = parseFloat(amountInput.value);

  if (!amount || amount <= 0) {
    status.textContent = lang === "jp"
      ? "有効な金額を入力してください。"
      : "Please enter a valid amount.";
    resultBox?.classList.add("hidden");
    return;
  }

  status.textContent = lang === "jp"
    ? "為替レートを取得中..."
    : "Fetching exchange rate...";

  try {
    const url = `https://open.er-api.com/v6/latest/${currency}`;
    const res = await fetch(url);
    const data = await res.json();

    if (!data || data.result !== "success" || !data.rates || !data.rates.JPY) {
      throw new Error("Invalid API response");
    }

    const rate = data.rates.JPY;
    const converted = amount * rate;

    inputSummary.textContent = `${amount.toLocaleString()} ${currency}`;
    rateSummary.textContent = `1 ${currency} = ${rate.toLocaleString()} JPY`;
    convertedSummary.textContent = `${converted.toLocaleString(undefined, {
      maximumFractionDigits: 2
    })} JPY`;

    resultBox?.classList.remove("hidden");

    status.textContent = lang === "jp"
      ? "換算結果を表示しました。"
      : "Conversion completed.";
  } catch (error) {
    console.error(error);
    status.textContent = lang === "jp"
      ? "為替レートの取得中にエラーが発生しました。"
      : "An error occurred while fetching the exchange rate.";
    resultBox?.classList.add("hidden");
  }
}

async function searchArt() {
  const input = document.getElementById("artSearchInput");
  const status = document.getElementById("artStatus");
  const resultsContainer = document.getElementById("artResults");
  const lang = localStorage.getItem("lang") || "jp";

  if (!input || !status || !resultsContainer) return;

  const query = input.value.trim();
  if (!query) {
    status.textContent = lang === "jp"
      ? "キーワードを入力してください。"
      : "Please enter a search keyword.";
    resultsContainer.classList.add("hidden");
    return;
  }

  status.textContent = lang === "jp" ? "検索中..." : "Searching...";
  resultsContainer.classList.add("hidden");

  try {
    const url = `https://api.artic.edu/api/v1/artworks/search?q=${encodeURIComponent(query)}&limit=6&fields=id,title,artist_display,date_display,medium_display,image_id,dimensions,classification_title`;
    const res = await fetch(url);
    const data = await res.json();

    if (!data || !data.data || data.data.length === 0) {
      status.textContent = lang === "jp"
        ? "該当する作品が見つかりませんでした。"
        : "No artworks found.";
      return;
    }

    const iiifBase = data.config?.iiif_url || "https://www.artic.edu/iiif/2";

    resultsContainer.innerHTML = data.data.map(item => {
      const imgSrc = item.image_id
        ? `${iiifBase}/${item.image_id}/full/843,/0/default.jpg`
        : "";
      return `
        <div class="art-result-card">
          ${imgSrc
            ? `<div class="art-img-wrap">
                <img src="${imgSrc}" alt="${item.title}" loading="lazy"
                      onerror="this.parentElement.innerHTML='<div class=\\'art-img-fallback\\'>Image not available</div>'" />
              </div>`
            : `<div class="art-img-wrap"><div class="art-img-fallback">No image</div></div>`
          }
          <div class="art-info">
            <h4>${item.title || "Untitled"}</h4>
            <p class="art-artist">${item.artist_display || "Unknown"}</p>
            <p class="art-meta">${[item.date_display, item.medium_display, item.classification_title].filter(Boolean).join(" · ")}</p>
            ${item.dimensions ? `<p class="art-meta">${item.dimensions}</p>` : ""}
          </div>
        </div>
      `;
    }).join("");

    resultsContainer.classList.remove("hidden");
    status.textContent = lang === "jp"
      ? `${data.data.length}件の作品が見つかりました。`
      : `${data.data.length} artworks found.`;

  } catch (error) {
    console.error(error);
    status.textContent = lang === "jp"
      ? "検索中にエラーが発生しました。"
      : "An error occurred during search.";
  }
}

